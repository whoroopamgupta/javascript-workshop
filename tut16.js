//  switch statement

//  dice : 1-6

let dice = 7;

// switch(condition){}

switch(dice){
    case 1 : 
    console.log('my value is 1');
    break;

    case 2: 
    console.log('value is 2');
    break;

    case 3:
        console.log('value is three');
        break;

    case 4:
        console.log('value is 4');    
        break;

    case 5 :
        console.log('value is 5');    
        break;

    case 6 :
        console.log('value is 6');    
        break;

        default : console.log('you did not roll the dice.');
}















